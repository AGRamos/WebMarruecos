				<h2 class="heading-title">Celebraciones</h2>
				<p>En Marruecos hay varios tipos de celebraciones. Están las celebraciones religiosas, las Fiestas Nacionales, los Moussems o festivales nacionales y los Festivales internacionales. 
				</p>
			</div>
			<div class="col-md-6 animate-box">
				<div class="row">
					<div class="col-md-12">
						<p>
						Las celebraciones religiosas no tiene fecha fija, ya que se rigen por la hejira, el calendario islámico, basado en el calendario lunar, y no coincide con el gregoriano (tiene alrededor de 11 días menos).
Estas festividades, que se celebran en todo el país, son: 
						</p>
					</div>
					<div class="col-md-12">
						<p> - Aid el Kebir o Aid Al Idha. Es la Fiesta del Cordero, se conmemora el sacrificio de Abraham.
						</p>
					</div>
					<div class="col-md-12">
						<strong>Enero</strong>
						<p> - Año Nuevo - 1º de Enero - Fiesta Nacional.
						- Manifiesto de la Independencia - 11 de enero - Fiesta Nacional de Marruecos. Se conmemora la presentacion del Manifiesto de la Independencia, reclamando la soberanía nacional.</p>
					</div>
					<div class="col-md-12">
						<strong>Febrero</strong>
						<p> - Fiesta de los Almendros, en el valle Ameln. Se celebra los últimos días del mes, cuando florecen los almendros, en todos los pueblos de los valles. Hay cantos, bailes y danzas típicas. </p>
					</div>
					<div class="col-md-12">
						<strong>Marzo-Abril</strong>
						<p> (De acuerdo al calendario musulmán, no tienen fecha fija)
- Festival de Moulav Aisaa Ben Driss, en Beni Mellal.<br>
- International Nomads Festival, en M'hamid. La celebracion tiene dos epicentros: uno a 2D km del pueblo de Beni Mellal, en un campamento especialmente construido para la ocasion, y el otro en el pueblo mismo. Dura cuatro días y llegan artistas de todas partes del mundo.<br>
- Moussem of Sidi Abdallah ibn Hassoun, en Sale. Consiste en una procesion que dura entre 3 y 4 horas, portando velas hacia la Gran Mezquita. </p>
					</div>
					<div class="col-md-12">
						<strong>Mayo</strong>
						<p>  	
- Fiesta del Trabajo - 1º de Mayo - Fiesta Nacional.
- Festiyal de las Rosas, en Kelaat Mgouna (Valle de Dades). Coincide con la recolección de las Rosas de Damasco. Durante la fiesta hay danzas, cantes y lluvia de petalos.<br>
- Festival de Música del desierto, en la región de TafiIaIet. Dura una semana y se presentan artistas de Arabia y Africa. La música es variada, desde el blues a composiciones folklóricas tradicionales. Se celebra en las ciudades de Errachidia, Rissani y Meknes en los últimos días del mes.<br>
- TANJAZZ, Tánger. Es en los últimos días de Mayo y participan importantes músicos de Estados Unidos y Europa.  </p>
					</div>
					<div class="col-md-12">
						<strong>Junio</strong>
						<p>  	
- Festival de arte, Marrakech.<br>
- Festival de Jerez, Sefrou.<br>
- Festival de Músicas Sacras, Fez. Uno de los más populares de Marruecos. Dura nueve días y muchos de los espectáculos son gratuitos. Se presentan artistas de todo el mundo.<br>
- Moussem Moulay Abdeslam, Larache.<br>
- Moussem de las Cerezas, Sefrou.<br>
- Festival de música Gnaoua y músicas etnicas, Essaouira.<br>
- Moussem Ben Aissa, Meknes. Uno de los moussems más tradicionales. Es la reunión anual de la Hermandad Aissoua. Dura varios días.  </p>
					</div>
					<div class="col-md-12">
						<strong>Julio</strong>
						<p>  	- Fiesta del Trono - 30 de Julio - Fiesta Nacional. Se conmemora la ascensión al trono del Rey Mohammed VI. La fiesta principal se realiza en el palacio, pero hay festejos en todas las ciudades y pueblos del país.<br>
- Festival del Camello, Guelmim. Transformado en una atracción turística, conserva sin embargo reminiscencias del festival original ya que todavía se danza la Guedra, en la que una mujer baila al son de un tambor. Se considera una ofrenda a Dios.<br>
- Festival de la Miel, Agadir.<br>
- Festival de Artes Populares, Marrakech. Del 14 al 19 de Julio. Se trata de una representación artística relacionada con las más profundas raíces culturales marroquíes: música bereber, danzas de los Atlas, música hipnótica del sur de Gnaouas, danza del vientre.<br>
- Festival Montreux de Jazz, Marrakech. 28 de Julio.<br>
- Festival Cultural Internacional, Asilah. Se ha celebrado durante los últimos 30 años y se presentan personalidades de la cultura mundial.<br>
- Festival "Alegría" en Chefchaouen, donde se presentan varios músicos del norte de Marruecos y sur de España. </p>
					</div>
					<div class="col-md-12">
						<strong>Agosto</strong>
						<p>  	- Fiesta de Marruecos del Rey y del Pueblo - 20 de Agosto - Fiesta Nacional. Conmemora el regreso al trono del rey Mohammed V, como resultado de la rebelión popular contra el rey impuesto por Francia, y por la liberación de Argelia y Túnez.<br>
- Fiesta de la Juventud - 21 de Agosto - Fiesta Nacional.<br>
- Moussem Setti Fatma, yalle de Ourika. Dura 4 días.<br>
- Moussem Moulay Abdallah, al sur de El Jadida.<br>
- Moussem Moulay Driss Zerhoun, al norte de Meknes. </p>
					</div>
					<div class="col-md-12">
						<strong>Septiembre</strong>
						<p>  	- Moussem Sidi Moussa Ou Quarqoui; cerca de Kelaat-Seraghna, al norte de Marrakech. Procesiones en honor del santo, hijo del Padre Fundador de Marruecos y creador de Fes.<br>
- Fiesta del Caballo "Fantasía", Tissa.<br>
- Festival de Marruecos Irnichil. Esta fiesta dura 3 días. Tradicionalmente, es el momento en que los jóvenes solteros de ambos sexos de la región se reúnen en busca de pareja, como en una multitudinaria cita a ciegas. Su origen es legendario y recuerda a dos jóvenes de diferentes tribus que, habiéndose enamorado, no pudieron unirse por prohibiciones familiares. Su tristeza fue tal que lloraron hasta morir, dando origen a los lagos Issly y TissIit. </p>
					</div>
					<div class="col-md-12">
						<strong>Octubre</strong>
						<p>  	- Mousses Moulav Idriss II, Fez<br>
- Festival de los Dátiles, Erfoud </p>
					</div>
					<div class="col-md-12">
						<strong>Noviembre</strong>
						<p>  	- Aniversario de la Marcha Verde - 6 de Noviembre - Fiesta Nacional. Conmemoración de la marcha iniciada el 6 de Noviembre de 1975 por ciudadanos y soldados marroquíes, bajo las órdenes del Rey Hassan II, con el fin de invadir y anexar el Sahara Occidental.<br>
- Fiesta de Marruecos de la Independencia - 18 de noviembre - Fiesta Nacional. Es la conmemoración del día en que el rey Mohammed y proclamó oficialmente la independencia de Francia y España, obtenida el 2 de marzo de 1956.  </p>
					</div>
					<div class="col-md-12">
						<strong>Diciembre</strong>
						 <p> - Festival de Marruecos Internacional de Cine, Marrakech. Se presentan más de 100 filmes en una semana. Atrae a grandes figuras de Hollywood cada año desde su creación en el 2001.<br>
- Fiesta de la cera, Sale.  </p>
